const avalicao = 5;

switch (avalicao) {
    case 5:
        console.log('Avaliação òtima');
        break
    case 4:
        console.log('Avaliação boa');
        break
    case 3:
        console.log('Avaliação neutra');
        break
    default:
        console.log('Avaliação baixa');
        break
}